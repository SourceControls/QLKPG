CREATE VIEW [dbo].[V_DOANH_THU]
AS
select kh.MAKH, HOTEN, SDT,hangKH,DOANHTHU = SUM(TIENKHACHTRA) 
from (select mapdk,TIENKHACHTRA from PHIEUTHUTIEN) PTT
	INNER JOIN (SELECT MAPDK, MAKH FROM PHIEUDK) PDK ON PTT.MAPDK = PDK.MAPDK
	INNER JOIN (SELECT MAKH, HOTEN, SDT,HANGKH FROM KHACHHANG) KH ON PDK.MAKH = KH.MAKH
GROUP BY KH.MAKH, HOTEN,SDT,HANGKH


