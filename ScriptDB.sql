CREATE PROC [dbo].[SP_UPDATE_TT_PDK]
AS
BEGIN
--QUÁ NGÀY CHỜ NỘP TIỀN LẦN ĐẦU 15 NGÀY
UPDATE PHIEUDK
SET TRANGTHAI = N'ĐÃ HỦY'
WHERE TRANGTHAI = N'CHỜ KÍCH HOẠT' AND DATEDIFF(DAY,NGAYDK,CONVERT(DATE, GETDATE())) >= 15

--SẮP HẾT HẠN TRƯỚC 3 NGÀY
UPDATE PHIEUDK
SET TRANGTHAI = N'SẮP HẾT HẠN'
WHERE (TRANGTHAI = N'ĐÃ KÍCH HOẠT')
		AND DATEDIFF(DAY,CONVERT(DATE, GETDATE()), NGAYKT) IN (1,2,3) 
--HẾT HẠN
UPDATE PHIEUDK
SET TRANGTHAI = N'HẾT HẠN'
WHERE (TRANGTHAI NOT IN (N'ĐÃ HỦY',N'HẾT HẠN') AND NGAYKT < CONVERT(DATE, GETDATE()))

--SẮP ĐẾN HẠN NỘP TIỀN
UPDATE PHIEUDK 
SET TRANGTHAI = N'SẮP ĐẾN HẠN NỘP TIỀN'
WHERE TRANGTHAI = N'ĐÃ KÍCH HOẠT'
	AND DATEDIFF(DAY, CONVERT(DATE, GETDATE()),(SELECT MAX(HANLANTOI)  
												FROM PHIEUTHUTIEN PTT 
												WHERE PTT.HANLANTOI is not null 
													AND PTT.MAPDK = PHIEUDK.MAPDK)) IN (1,2,3);
--ĐÃ HỦY(HẾT HẠN NỘP)
UPDATE PHIEUDK
SET TRANGTHAI = N'ĐÃ HỦY'
WHERE  (TRANGTHAI = N'SẮP ĐẾN HẠN NỘP TIỀN') AND 
		(SELECT MAX(HANLANTOI) 
			FROM PHIEUTHUTIEN PTT 
			WHERE PTT.HANLANTOI is not null AND PTT.MAPDK = PHIEUDK.MAPDK) < CONVERT(DATE, GETDATE());
END

